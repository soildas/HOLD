create table products (
    num char(20) not null,
    img char(20) not null,
    brand char(20) not null,
    name char(20) not null,
    kategorie char(20) not null,
    color char(20) not null,
    size char(15) not null,
    price char(15) not null,
    regtime char(20),
    primary key(num)
);
